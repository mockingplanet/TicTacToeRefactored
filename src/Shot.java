public class Shot {
    /**
     * Estas son variables para la fila y la columna
     */
    public int row, col;

    /**
     * Constructor que los inicializa
     * @param row la fila
     * @param col la columna
     */
    public Shot(int row, int col) {
        this.row = row;
        this.col = col;
    }
}
